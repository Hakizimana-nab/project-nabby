        What is Python? Executive Summary<br>
        Python is an interpreted, object-oriented, high-level programming language<br>
        with dynamic semantics. Its high-level built in data structures, <br>
        with dynamic typing and dynamic binding, make it very attractive for Rapid Application Development,<br>
        as well as for use as a scripting or glue language to connect existing components together.<br>
        Python's simple, easy to learn syntax emphasizes readability and therefore reduces the cost of program maintenance.<br>
        Python supports modules and packages, which encourages program modularity and code reuse. <br>
        The Python interpreter and the extensive standard library are available in source or binary form without charge for all major platforms,<br>
        and can be freely distributed.
        
           <ul>learn more on Python?</ul>

        Often, programmers fall in love with Python because of the increased productivity it provides.<br>
        Since there is no compilation step, the edit-test-debug cycle is incredibly fast.<br>
        Debugging Python programs is easy: a bug or bad input will never cause a segmentation fault.<br>
        Instead, when the interpreter discovers an error, it raises an exception.<br>
        When the program doesn't catch the exception, the interpreter prints a stack trace.<br>
        A source level debugger allows inspection of local and global variables,<br>
        evaluation of arbitrary expressions, setting breakpoints,<br>
        stepping through the code a line at a time, and so on.<br>
        The debugger is written in Python itself, testifying to Python's introspective power.<br>
        On the other hand, often the quickest way to debug a program is to add<br>
        a few print statements to the source: the fast edit-test-debug cycle makes this simple approach very effective.

